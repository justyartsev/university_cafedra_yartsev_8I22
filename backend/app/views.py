from rest_framework import viewsets
from rest_framework.response import Response
from rest_framework import status
from .models import Professor, Office, Degree, Position, WorkTime, Discipline, TeachingType, ProfessorDiscipline, OtherActivity
from .serializers import ProfessorSerializer, OfficeSerializer, DegreeSerializer, PositionSerializer, WorkTimeSerializer, DisciplineSerializer, TeachingTypeSerializer, ProfessorDisciplineSerializer, OtherActivitySerializer

class OfficeViewSet(viewsets.ModelViewSet):
    queryset = Office.objects.all()
    serializer_class = OfficeSerializer

    def create(self, request, *args, **kwargs):
        serializer = self.get_serializer(data=request.data)
        if serializer.is_valid():
            self.perform_create(serializer)
            return Response(serializer.data, status=status.HTTP_201_CREATED)
        return Response(serializer.errors, status=status.HTTP_400_BAD_REQUEST)

    def update(self, request, *args, **kwargs):
        partial = kwargs.pop('partial', False)
        instance = self.get_object()
        serializer = self.get_serializer(instance, data=request.data, partial=partial)
        if serializer.is_valid():
            self.perform_update(serializer)
            return Response(serializer.data)
        return Response(serializer.errors, status=status.HTTP_400_BAD_REQUEST)

    def destroy(self, request, *args, **kwargs):
        instance = self.get_object()
        self.perform_destroy(instance)
        return Response(status=status.HTTP_204_NO_CONTENT)

